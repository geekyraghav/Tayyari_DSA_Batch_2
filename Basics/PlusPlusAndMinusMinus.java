package Basics;

public class PlusPlusAndMinusMinus {
    public static void main(String[] args) {
        int x = 10;
        x++; // x = x + 1 or x += 1
        System.out.println(x);
        ++x;
        System.out.println(x);
        x--;
        System.out.println(x);
        --x;
        System.out.println(x);
    }
}
