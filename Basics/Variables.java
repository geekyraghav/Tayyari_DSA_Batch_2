package Basics;

public class Variables {
    public static void main(String[] args) {
        int x; // declaration
        x = 10; // initialization
        System.out.println("x");
        System.out.println(x);
        x = 45;
        System.out.println(x);
        x = -3;
        System.out.println(x);
        int y = 8;
        System.out.println(x*y);
    }
}
